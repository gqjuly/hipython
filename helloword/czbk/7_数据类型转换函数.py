# float()类型转换
num1 = 1
str1 = '10'
print(type(float(num1)))        #float
print(float(num1))      #1.0
print(float(str1))      #10.0

# str()类型转换
print(type(str(num1)))      #str


# tuple()类型转换
list1 = [10, 20, 30]
print(type(tuple(list1)))       #tuple
print(tuple(list1))     #(10, 20, 30)


# list()类型转换
t1 = (100, 200, 300)
print(type(list(t1)))


